package com.enteractive.trees;

/**Class for Points on Arrow
 * @param <X> coordinate
 * @param <Y> coordinate
 */
public class Points<X,Y> {
    public X item1;
    public Y item2;

    public Points(X item1, Y item2) {
        this.item1 = item1;
        this.item2 = item2;
    }
}
